function grade_password(password) {
    if(password.length==0)
    {
        return -1;
    }
    if(password.match(/^(?=.*[\!\@\#\$\%\^\&\*\(\)\-\_\=\+\[\]\{\}\;\:\'\"\<\>\,\.\\\|\/\?])(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9]).{8,255}$/))
    {
        return 3;
    }
    else if(password.match(/^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9]).{8,255}$/) || password.match(/^(?=.*[\!\@\#\$\%\^\&\*\(\)\-\_\=\+\[\]\{\}\;\:\'\"\<\>\,\.\\\|\/\?])(?=.*?[a-z])(?=.*?[0-9]).{8,255}$/) || password.match(/^(?=.*?[A-Z])(?=.*[\!\@\#\$\%\^\&\*\(\)\-\_\=\+\[\]\{\}\;\:\'\"\<\>\,\.\\\|\/\?])(?=.*?[0-9]).{8,255}$/))
    {
        return 2;
    }
    else if(password.match(/^(?=.*?[a-z])(?=.*?[0-9]).{8,255}$/) || password.match(/^(?=.*?[A-Z])(?=.*?[0-9]).{8,255}$/))
    {
        return 1;
    }
    else
    {
        return 0;
    }
}